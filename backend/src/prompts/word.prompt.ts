/**
 * 让模型优先输出“义项 + 典型短语化场景”，而不是松散的完整例句。
 */
export function buildWordPrompt(word: string): string {
  return `
你是一个英语单词记忆助手。

任务：
围绕单词 "${word}"，按考研英语里常见、常考的意思生成记忆材料。

要求：
1. 先判断这个单词在考研英语中的高频常用义项，再决定输出几组内容
2. 只输出高频、常用、容易混淆或值得记的义项，不要为了凑数硬编冷僻义项
3. 一般返回 1 到 4 组；如果只有 1 到 2 个高频义项，就只返回 1 到 2 组
4. 每组都必须包含：
   - partOfSpeech：词性，使用标准缩写，例如 n., v., adj., adv.
   - meaning：该义项的标准中文释义，要像词典里的常见义项，短、准、规范
   - example：该义项下最典型、最好记的英文短语或短场景
   - tip：中文联想短语，要帮助用户立刻形成画面感或动作感
5. partOfSpeech 必须符合这些要求：
   - 只写最贴切的一个词性
   - 使用标准英语词性缩写，例如 n., v., adj., adv., prep.
6. meaning 必须符合这些要求：
   - 不要写成解释句，不要写“用力举起或搬动物”这种描述性句子
   - 尽量用标准义项表达，例如“举起；搬动”“起伏”“喘息”
   - 控制在 2 到 8 个字，必要时可稍长，但不要啰嗦
7. example 不要优先写完整例句，要优先写这种风格的短语化表达：
   - heave a box
   - heave it up
   - the sea heaved
   - his chest heaved
   - heave the bag away
8. example 可以是：
   - 动词 + 宾语
   - 主语 + 动词
   - 动词 + 副词 / 介词
   - 很短的小场景
9. example 要短、准、常见，最好一眼就能看出这个词在真实场景里怎么用；不要写空泛句子，不要写复杂从句，不要堆生词
10. 如果一个单词有多个常见用法，要优先覆盖“不同义项”或“同一义项下最典型的不同场景”，而不是只换主语重复造句
11. tip 不要只是翻译 example；要把这个搭配变成脑中能看到的画面、动作、人物或场景
12. tip 必须非常短，像联想标签，不要写完整句，不要加“想象”“记住”“一下”等铺垫
13. tip 最好控制在 4 到 12 个字，风格参考：
   - 弯腰猛搬重箱
   - 海面上下翻涌
   - 胸口剧烈起伏
14. 不要输出解释，不要输出多余文字
15. 必须严格返回 JSON

返回格式：
{
  "word": "${word}",
  "meanings": [
    {
      "partOfSpeech": "词性",
      "meaning": "义项 1",
      "example": "example 1",
      "tip": "tip 1"
    }
  ]
}
`.trim()
}
